#include "checker.h"

int	count_arg(int ac, char ***av)
{
	int		i;
	int		j;
	int		nbr_opt;

	i = 1;
	j = 0;
	while (i < ac && (*av)[i][0] == '-')
		i++;
	nbr_opt = i;
	while (i++ < ac)
		j++;
	(*av) += nbr_opt;
	return (j);
}
