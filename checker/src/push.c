#include "push_swap.h"

void	push(t_list **lst_a, t_list **lst_b)
{
	t_list	*tmp;

	if (!*lst_a)
		return ;
	tmp = *lst_b;
	*lst_b = *lst_a;
	*lst_a = (*lst_a)->next;
	(*lst_b)->next = tmp;
}
